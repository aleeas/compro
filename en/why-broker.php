						<h3>What Does Insurance Broker Mean?</h3>
						<p>Insurance Broker is a legal entity that is established in order to
						fulfil society’s need of an entity that can assist them in buying
						insurance products and advocate with claim reimbursement,
						during which community/the insured/customer is unfamiliar
						with the terms and conditions of the insurance policy.</p>

						<p>The government acknowledged the need to assert insurance
						broker’s foundation through regulations; namely the Law of
						Insurance No. 2/1992, which aims to protect public interest.
						The roles and functions of insurance broker all over the world
						have been developed and almost every insurance transactions
						are done through insurance brokering.</p>

						<p>Insurance broker is established as a legal entity and must own
						a license from the Financial Services Authority (OJK) which
						carries strict requirements that have been regulated in the :<br>
						-  Law of the Republic of Indonesia about Insurance Business  No. 2/1992.
						<br>
						- Government Regulation No. 73/1992.<br>
						- Decree of the Minister of Finance of the Republic of Indonesia
 						No. 226/KMK.017/1993.<br>
						- Regulation of the Financial Services Authority
 						No. 70/POJK.05/2016 and 68/POJK.05/2016</p>

						<p>The Law No. 2/1992 Article 1 Verse 8 states that “Insurance
						Broker is an enterprise that provides intermediary services in
						insurance coverage and claim settlement handling by acting
						as a representative of the insured”.</p>

						<p>Article 5 Verse 1 states that “Insurance Broker can only organize
						business by representing the insured in transactions
						related to insurance contract”. </p>

						<blockquote><p>Insurance broker is different from insurance agent. Insurance broker represents on behalf of the insured’s interest, while insurance agent acts on behalf of the insurance company. Insurance agent does not need a license and operates under Financial Services Authority (OJK) supervision.</p></blockquote>
